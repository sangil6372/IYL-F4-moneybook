// ÌéòÏù¥ÏßÄÎÑ§Ïù¥ÏÖò Í∑∏Î£π Í≥ÑÏÇ∞
// 4 -> 0 / 6 -> 1
const currentGroup = computed(() =>
  Math.floor((currentPage.value - 1) / pageGroupSize)
