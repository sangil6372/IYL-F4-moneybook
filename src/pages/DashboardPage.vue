  const start = (currentPage.value - 1) * itemsPerPage;
  const end = start + itemsPerPage;
  return filteredTransaction.value.slice(start, end);
});

// Îç∞Ïù¥ÌÑ∞ ÏÇ≠Ï†ú Ìï†ÏßÄ Î¨ºÏñ¥Î≥¥Í∏∞ Ìò∏Ï∂ú
